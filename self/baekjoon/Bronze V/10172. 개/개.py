# 역슬래시를 단독으로 사용하기 위해서 연속으로 두개를 적어야함 (\\ )
# 큰따옴표를 출력해야 되기 때문에 작은 따옴표로 문자열 감쌈
print("|\\_/|")
print("|q p|   /}")
print('( 0 )"""\\')
print('|"^"`    |')
print("||_/=\\\\__|")
# 작은따옴표를 둘러싸지 않고 그 문자 자체를 사용하기 위해서 (\") 를 사용함
print("|\\_/|")
print("|q p|   /}")
print("( 0 )\"\"\"\\")
print("|\"^\"`    |")
print("||_/=\\\\__|")
